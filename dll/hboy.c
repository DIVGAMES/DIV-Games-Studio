void buffer_to_video1() {
  char *di,*si;
  int yr,xr,iyr,ixr,an,al;

  ixr=(wide<<8)/160; iyr=(height<<8)/100; yr=0;
  di=(char*)0xA0000+320*50+80; al=100;

  do {
    si=buffer+(yr>>8)*wide; an=160; xr=0;
    do {
      *di=xlat[*(si+(xr>>8))];
      di++; xr+=ixr;
    } while (--an);
    yr+=iyr; di+=160;
  } while (--al);
}

