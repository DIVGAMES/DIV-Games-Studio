void put_hboy() {
  unsigned int con,X=0,RX=0,Y=0;
  unsigned int fExit=0;
  unsigned int pixel=0;
  char ch,rep;
  byte *q = (byte *)screen->pixels;

  hboy=_hboy;
  
  while(!fExit) {
    ch=*hboy++;
    rep=1;
    if((ch&192)==192) { rep=(ch&63); ch=*hboy++;}
		pixel+=rep;
		for(con=0;con<rep;con++) {  
			hboy_graph[RX]=ch;
			q[X++]=ch; 
			RX++; 
			if(RX>=64000) 
				fExit=1; 
			if(RX%320==0) {
				Y++;
				X=Y*screen->pitch;
	//			printf("new line: %d %d\n",X,Y);
			}
		}
	}
}

